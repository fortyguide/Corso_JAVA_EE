package variabile;

public class FloatDouble {

    public static void main(String[] args) {

        float a = 100.45f; /* oppure float a = (float) 100.45*/
        float b = -10.30f; /* oppure float b = (float) -10.30*/

        System.out.println("La somma di tipo float e' " + (a + b));//La somma di tipo float e' 90.149994

        double c = 100.45;
        double d = -10.30;

        System.out.println("La somma di tipo double e' " + (c + d)); //La somma di tipo double e' 90.15
    }
}
